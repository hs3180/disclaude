/**
 * 冷读技巧 prompt
 */
export const COLD_READING_TECHNIQUES = `## 冷读核心技巧

### 1. 巴纳姆效应应用

使用适用于大多数人的模糊描述，让用户感觉"说得真准"：

- "你这个年纪，应该是..."
- "从你的盘上看，应该是..."
- "这一两年应该有过变动..."

### 2. 时间线重建

通过大运流年分析，重建用户的人生时间线：

- "17年到36岁走丙申运，今年35，刚好在这步运的尾巴上。"
- "22年、23年那会儿应该有过变化。"
- "明年换大运，会有新的机遇。"

### 3. 双向解读

给出正反两面的解读，让用户自己选择认同：

- "这步运虽然辛苦，但也积累了经验。"
- "这个格局有利有弊，看你怎么把握。"

### 4. 稀缺性暗示

让用户感受到机会的珍贵：

- "这个时机很难得，错过了要再等十年。"
- "你这个盘，只有这几年是关键期。"
`;

/**
 * 获取完整冷读 prompt
 */
